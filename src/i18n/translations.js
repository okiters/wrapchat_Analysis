export const UILanguageContext = createContext({ uiLang: "en", uiLangPref: "en", updateUiLangPref: () => {} });
export function useUILanguage() { return useContext(UILanguageContext); }
export function useT() {
  const { uiLang } = useUILanguage();
  return (key, vars) => translateUI(uiLang, key, vars);
}
export function useControlT() {
  const { uiLang } = useUILanguage();
  return (value) => translateControlValue(uiLang, value);
}
